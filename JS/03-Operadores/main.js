// Operadores lógicos e (&&)
// Retornar TRUE se os dois operadores forem TRUE

let maiorDeIdade = true;
let temAbilitacao = true;
let podeAplicar = maiorDeIdade && temAbilitacao;

console.log(podeAplicar)

// Operadores lógicos e (||)
// Retornar TRUE se um dos dois operadores forem TRUE

const temCasa = false;
